let store: EmployeeLeave[] = EMPLOYEE_LEAVES_SEED.map((r) => ({ ...r }));
let nextLeaveSeq = 25;

export function getEmployeeLeavesStore(): EmployeeLeave[] {
  return store.filter((r) => r.recordStatus === 1).map((r) => ({ ...r }));
}

export function resetEmployeeLeavesStore(): void {
  store = EMPLOYEE_LEAVES_SEED.map((r) => ({ ...r }));
  nextLeaveSeq = 25;
}

export function allocateLeaveId(): string {
  const id = `LV-${String(nextLeaveSeq).padStart(3, '0')}`;
  nextLeaveSeq += 1;
  return id;
}

export function appendLeaveRecord(row: EmployeeLeave): void {
  store = [...store, row];
}

export function updateLeaveRecord(id: string, patch: Partial<EmployeeLeave>): EmployeeLeave | undefined {
  const idx = store.findIndex((r) => r.id === id);
  if (idx < 0) return undefined;
  const next = { ...store[idx]!, ...patch };
  store = [...store.slice(0, idx), next, ...store.slice(idx + 1)];
  return { ...next };
}

export function getLeaveById(id: string): EmployeeLeave | undefined {
  const r = store.find((x) => x.id === id && x.recordStatus === 1);
  return r ? { ...r } : undefined;
}
