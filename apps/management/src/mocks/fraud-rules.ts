import type { FraudRuleRecord } from '@/features/risk-compliance/fraud-rules/domain/types';
import type { FraudRuleAction } from '@/features/risk-compliance/fraud-rules/domain/types';
import type { FraudAction } from '@/features/risk-compliance/shared/fraud-actions';

function actions(types: FraudAction[], blockTarget?: 'Transaction' | 'Wallet' | 'Customer'): FraudRuleAction[] {
  return types.map((type) => {
    if (type === 'Block') {
      return {
        type,
        params: { target: blockTarget ?? 'Transaction', durationMinutes: 120 },
      };
    }
    if (type === 'AddRisk') {
      return { type, params: { target: 'Customer', durationDays: 30, riskPoints: 15 } };
    }
    if (type === 'CreateCase') {
      return { type, params: { severity: 'High' } };
    }
    return { type };
  });
}

function rule(
  partial: Omit<FraudRuleRecord, 'actionDetails' | 'recordStatus'> & {
    recordStatus?: 0 | 1;
    actionTypes?: FraudAction[];
    actionDetails?: FraudRuleAction[];
  },
): FraudRuleRecord {
  const ts = '2026-05-01T10:00:00Z';
  const { recordStatus = 1, actionTypes, actionDetails, ...rest } = partial;
  return {
    recordStatus,
    description: '',
    regulationReference: '',
    dslValidatedAt: ts,
    createdAt: ts,
    updatedAt: ts,
    actionDetails: actionDetails ?? actions(actionTypes ?? ['CreateCase']),
    ...rest,
  };
}

