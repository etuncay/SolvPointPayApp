export async function ensureCustomerPortalSeeded(): Promise<void> {
  const db = getDb();
  const existing = await db.meta.get(META_KEY);
  if (existing?.value === SEED_VERSION) return;

  const seed = getCustomerPortalSeed();
  await db.transaction(
    'rw',
    [
      db.customerProfile,
      db.customerWallets,
      db.customerTransactions,
      db.customerRecipients,
      db.customerIbans,
      db.customerFees,
      db.customerSettings,
      db.customerTopup,
      db.customerReceipts,
      db.customerContacts,
      db.meta,
    ],
    async () => {
      await db.customerProfile.clear();
      await db.customerWallets.clear();
      await db.customerTransactions.clear();
      await db.customerRecipients.clear();
      await db.customerIbans.clear();
      await db.customerFees.clear();
      await db.customerSettings.clear();
      await db.customerTopup.clear();
      await db.customerReceipts.clear();
      await db.customerContacts.clear();

      await db.customerProfile.put(seed.profile);
      await db.customerWallets.bulkPut(seed.wallets);
      await db.customerTransactions.bulkPut(seed.transactions);
      await db.customerRecipients.bulkPut(seed.recipients);
      await db.customerIbans.bulkPut(seed.ibans);
      await db.customerFees.bulkPut(seed.fees);
      await db.customerSettings.put(seed.settings);
      await db.customerTopup.put(seed.topup);
      await db.customerReceipts.bulkPut(seed.receipts);
      await db.customerContacts.bulkPut(seed.contacts);
      await db.meta.put({ key: META_KEY, value: SEED_VERSION });
    },
  );
}
