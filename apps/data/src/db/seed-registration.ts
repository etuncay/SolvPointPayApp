/** Tarayıcı IndexedDB — boşsa lookup demo kayıtlarını üretir. */
export async function ensureAgentRegistrationsSeeded(): Promise<void> {
  const db = getDb();
  const count = await db.agentRegistrations.count();
  if (count > 0) return;
  await db.agentRegistrations.bulkPut(SEED.map((r) => ({ ...r })));
  await db.meta.put({ key: 'nextRegistrationId', value: 200009 });
}
