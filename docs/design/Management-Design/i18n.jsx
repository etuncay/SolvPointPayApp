function useT(lang) {
  return React.useCallback((k) => (STRINGS[lang] && STRINGS[lang][k]) ?? STRINGS.tr[k] ?? k, [lang]);
}

// Money formatter (TRY)
function fmtMoney(n, lang = "tr") {
  const locale = lang === "tr" ? "tr-TR" : "en-US";
  return new Intl.NumberFormat(locale, {
    style: "currency",
    currency: "TRY",
    maximumFractionDigits: 0,
  }).format(n);
}
function fmtNumber(n, lang = "tr") {
  const locale = lang === "tr" ? "tr-TR" : "en-US";
  return new Intl.NumberFormat(locale).format(n);
}
function fmtAge(minutes, lang = "tr") {
  if (minutes < 1) return lang === "tr" ? "şimdi" : "now";
  if (minutes < 60) return `${minutes}${lang === "tr" ? "dk" : "m"}`;
  const h = Math.floor(minutes / 60);
  const m = minutes % 60;
  return `${h}${lang === "tr" ? "sa" : "h"} ${m}${lang === "tr" ? "dk" : "m"}`;
}

Object.assign(window, { STRINGS, useT, fmtMoney, fmtNumber, fmtAge });
